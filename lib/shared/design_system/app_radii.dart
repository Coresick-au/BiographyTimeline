import 'package:flutter/material.dart';

/// Strict border radius tokens for the Users Timeline application.
class AppRadii {
  AppRadii._();

  /// 4.0 - Minimal rounding
  static const double xs = 4.0;

  /// 8.0 - Small rounding (buttons, inputs)
  static const double sm = 8.0;

  /// 12.0 - Medium rounding (cards)
  static const double md = 12.0;

  /// 16.0 - Large rounding (dialogs, prominent cards)
  static const double lg = 16.0;

  /// 24.0 - Extra large rounding (bottom sheets)
  static const double xl = 24.0;

  /// 999.0 - Full circle (avatars, pills)
  static const double circular = 999.0;
}
