import 'package:flutter/material.dart';

/// Strict spacing tokens for the Users Timeline application.
/// Usage: `AppSpacing.md` (16.0) or `Gap(AppSpacing.md)`
class AppSpacing {
  AppSpacing._();

  /// 4.0 - Tight spacing for related items
  static const double xs = 4.0;

  /// 8.0 - Default small spacing
  static const double sm = 8.0;

  /// 12.0 - Medium-small spacing
  static const double md = 12.0;

  /// 16.0 - Standard moderate spacing
  static const double lg = 16.0;

  /// 24.0 - Section spacing
  static const double xl = 24.0;

  /// 32.0 - Large section spacing
  static const double xxl = 32.0;

  /// 48.0 - Huge spacing for major divisions
  static const double xxxl = 48.0;

  /// 64.0 - Massive spacing for hero sections
  static const double huge = 64.0;
}
